
//change a value in program ?:- 
import java.util.*;

public class Classroom {

    public static void main(String[] args) {
        // Swap-values exchanges
        int a = 5;
        int b = 10;
        // swap
        int temp = a;
        a = b;
        b = temp;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
/*
 * Output:-
 * a = 10
 * b = 5
 */
